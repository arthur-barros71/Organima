            <p class="Quest1 fontcorpo">“Como surgiu a Organima?”:</p>
            <p class="TextQuest1 fontcorpo">A ferramenta foi idealizada inicialmente como um local onde o usuário
                poderia colocar seus trabalhos para facilitar o encontro entre produtores e aqueles que trabalham de
                forma independente. Porém, com o polimento de ideias, a equipe desenvolvedora decidiu focar em apenas
                uma das áreas de interesse: o setor de animação. Sendo assim, o objetivo da empresa se voltou a
                contribuir significativamente para a organização dos projetos de animação ou. Mas percebemos que isso
                não seria o suficiente, então surgiu a ideia de detectar erros de continuidade. Desde então, nós
                trabalhamos para contribuir para a criação das animações com o nome de Organima.</p>

